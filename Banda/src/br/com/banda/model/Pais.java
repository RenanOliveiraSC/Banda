package br.com.banda.model;

public class Pais {

	private int pai_Codigo;
	private String pai_Nome;

	public int getPai_Codigo() {
		return pai_Codigo;
	}

	public void setPai_Codigo(int pai_Codigo) {
		this.pai_Codigo = pai_Codigo;
	}

	public String getPai_Nome() {
		return pai_Nome;
	}

	public void setPai_Nome(String pai_Nome) {
		this.pai_Nome = pai_Nome;
	}

}
